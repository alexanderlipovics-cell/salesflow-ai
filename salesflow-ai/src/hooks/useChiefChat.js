import { useState, useCallback, useRef, useEffect } from 'react';
import {
  sendMessageToChief,
  handleChiefActions,
  getSuggestedPrompts,
} from '../services/chiefService';
import { useChiefDailyFlowContext } from './useChiefDailyFlowContext';
